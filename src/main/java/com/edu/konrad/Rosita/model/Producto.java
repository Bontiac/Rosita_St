package com.edu.konrad.Rosita.model;

public class Producto {
    private String nombre;
    private int valor;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getAValor() {
        return valor;
    }

    public void setApellido(int valor) {
        this.valor = valor;
    }

}
